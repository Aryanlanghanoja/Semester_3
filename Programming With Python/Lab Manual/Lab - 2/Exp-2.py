X = 5
Y = 2

print(f"Addition of {X} and {Y} Is {X + Y}")
print(f"Substraction of {X} and {Y} Is {X - Y}")
print(f"Multiplication of {X} and {Y} Is {X*Y}")
print(f"Floating Point Division of {X} and {Y} Is {X / Y}") 
print(f"Floor Division of {X} and {Y} Is {X//Y}") 
print(f"Modulo Operation of {X} and {Y} Is {X%Y}")
print(f"{X} raised to {Y} Is {X**Y}")